function renderFloatingChats(feedType) {
    const container = document.getElementById(`${feedType}-chat-feed`); 
    if(!container) return;
    container.innerHTML = '';
    const store = feedType === 'notes' ? notesChatStore : exercisesChatStore;

    if(store.length === 0) {
        container.innerHTML = `<div class="empty-chat-lbl">No comments yet. Start the conversation!</div>`; 
        return;
    }

    store.forEach(msg => {
        const bubble = document.createElement('div'); 
        bubble.className = 'chat-bubble-item';
        bubble.innerHTML = `
            <div class="chat-bubble-meta">
                <span class="chat-user">@${msg.name}</span>
                <span class="chat-time">${msg.timestamp}</span>
            </div>
            <div class="chat-bubble-text">${msg.text}</div>
        `;
        container.appendChild(bubble);
    });
    container.scrollTop = container.scrollHeight;
}

function postFloatingComment(event, feedType) {
    event.preventDefault();
    const nameNode = document.getElementById(`${feedType}-chat-name`);
    const textNode = document.getElementById(`${feedType}-chat-text`);
    if(!nameNode.value.trim() || !textNode.value.trim()) return;

    const now = new Date();
    const timeStr = String(now.getHours()).padStart(2, '0') + ':' + String(now.getMinutes()).padStart(2, '0');
    
    const newMsg = { 
        name: nameNode.value.trim(), 
        text: textNode.value.trim(), 
        timestamp: timeStr, 
        createdDate: now.toISOString() 
    };

    syncFeedbackBoardOnline(feedType, "POST", newMsg);
    textNode.value = ''; 
}

