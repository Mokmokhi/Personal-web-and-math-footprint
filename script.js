async function syncFeedbackBoardOnline(feedType, action = "GET", payload = null) {
    try {
        if (action === "GET") {
            const res = await fetch(`${LIVE_DB_FEEDBACK_API}/${feedType}.json`);
            if (!res.ok) throw new Error("Cloud fetch request interrupted.");
            const data = await res.json();
            
            const convertedList = [];
            if (data) {
                Object.keys(data).forEach(key => {
                    convertedList.push({ id: key, ...data[key] });
                });
            }
            if (feedType === 'notes') notesChatStore = convertedList;
            else exercisesChatStore = convertedList;
            
            renderFloatingChats(feedType);
        } else if (action === "POST" && payload) {
            const res = await fetch(`${LIVE_DB_FEEDBACK_API}/${feedType}.json`, {
                method: "POST",
                headers: { "Content-Type": "application/json" },
                body: JSON.stringify(payload)
            });
            if (!res.ok) throw new Error("Cloud sync transmit failed.");
            await syncFeedbackBoardOnline(feedType, "GET");
        }
    } catch (err) {
        console.warn("Internet database sync offline: ", err);
        if (action === "POST" && payload) {
            const store = feedType === 'notes' ? notesChatStore : exercisesChatStore;
            store.push({ id: Date.now(), ...payload });
            renderFloatingChats(feedType);
        }
    }
}

