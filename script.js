function applyTranslations() {
    document.getElementById('lang-label').innerText = currentLang.toUpperCase();
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.innerHTML = translations[currentLang][key];
        }
    });
    
    const targetMap = translations[currentLang];
    if (document.getElementById('wardrobe-search-bar')) {
        document.getElementById('wardrobe-search-bar').placeholder = targetMap["ph-search-notes"];
    }
    if (document.getElementById('exercises-search-bar')) {
        document.getElementById('exercises-search-bar').placeholder = targetMap["ph-search-exercises"];
    }
    
    const nicknameInputs = ['notes-chat-name', 'exercises-chat-name'];
    const msgTextareas = ['notes-chat-text', 'exercises-chat-text'];
    
    nicknameInputs.forEach(id => {
        if (document.getElementById(id)) document.getElementById(id).placeholder = targetMap["ph-chat-nickname"];
    });
    msgTextareas.forEach(id => {
        if (document.getElementById(id)) document.getElementById(id).placeholder = targetMap["ph-chat-msg"];
    });

    renderAdvancedBackgroundBubbles();
}

