function sortAlphabetically(array) {
    return array.sort((a, b) => {
        const tA = currentLang === 'zh' ? (a.title_zh || '') : (a.title_en || '');
        const tB = currentLang === 'zh' ? (b.title_zh || '') : (b.title_en || '');
        return tA.localeCompare(tB);
    });
}

function matchSearchQuery(item, queryStr) {
    if (!item) return false;
    if (!queryStr) return true;
    const cleanQuery = queryStr.toLowerCase().trim();
    
    const title = currentLang === 'zh' ? (item.title_zh || '') : (item.title_en || '');
    const body = currentLang === 'zh' ? (item.body_zh || '') : (item.body_en || '');
    
    const matchesTitle = title.toLowerCase().includes(cleanQuery);
    const matchesBody = body.toLowerCase().includes(cleanQuery);
    const matchesCategory = (item.categories || []).some(cat => cat.toLowerCase().includes(cleanQuery));
    const matchesFiles = (item.filesList || []).some(filePath => filePath.split('/').pop().toLowerCase().includes(cleanQuery));
    
    return matchesTitle || matchesBody || matchesCategory || matchesFiles;
}

